public class Aritmetica 
{
    // método main inicia a execusão do aplicativo Java
    public static void main(String[] args) 
    {        
        // váriaveis do tipo int
        int a = 10; 
        int b = 2;
        int c = 3;
        int d = 4;
        int e = 5;
        int soma;

        soma = a * b / c + (d - e); // efetua o calculo aritmético
        System.out.println(soma);
    } // fim do método main 
} // fim da classe Aritmetica
